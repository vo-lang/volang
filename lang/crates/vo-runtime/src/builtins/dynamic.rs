#[allow(clippy::too_many_arguments)]
fn do_call(
    call: &mut ExternCallContext,
    closure_ref: GcRef,
    sig_rttid: u32,
    args_slice_ref: GcRef,
    ret_count: usize,
    metas_start: u16,
    is_any_start: u16,
    error_offset: u16,
) -> ExternResult {
    if call.is_replay_panicked() {
        let msg = replay_panic_cow(call);
        return call_return_error(call, error_offset, DynErr::BadCall, &msg);
    }
    if closure_ref.is_null() {
        return call_return_error(call, error_offset, DynErr::BadCall, "closure is null");
    }

    // Get function signature
    let (params, ret_value_rttids, is_variadic) = match call.get_func_signature(sig_rttid) {
        Some((p, r, v)) => (p.clone(), r.clone(), v),
        None => {
            return call_return_error(
                call,
                error_offset,
                DynErr::BadCall,
                "invalid closure signature",
            );
        }
    };

    // Check return count
    let actual_ret_count = ret_value_rttids.len();
    if ret_count != actual_ret_count {
        let msg = format!(
            "return count mismatch: expected {} return value(s), function returns {} (hint: adjust LHS variable count to match function signature)",
            ret_count, actual_ret_count
        );
        return call_return_error(call, error_offset, DynErr::SigMismatch, &msg);
    }

    // Check parameter count
    let arg_count = if args_slice_ref.is_null() {
        0
    } else {
        slice::len(args_slice_ref)
    };
    let expected_params = if is_variadic && !params.is_empty() {
        params.len() - 1
    } else {
        params.len()
    };

    if !is_variadic && arg_count != expected_params {
        let msg = format!(
            "parameter count mismatch: expected {}, got {}",
            expected_params, arg_count
        );
        return call_return_error(call, error_offset, DynErr::SigMismatch, &msg);
    }
    if is_variadic && arg_count < expected_params {
        let msg = format!(
            "parameter count mismatch: expected at least {}, got {}",
            expected_params, arg_count
        );
        return call_return_error(call, error_offset, DynErr::SigMismatch, &msg);
    }

    // Unpack args
    let args = match unpack_args(call, args_slice_ref, &params, is_variadic) {
        Ok(a) => a,
        Err(_) => {
            return call_return_error(
                call,
                error_offset,
                DynErr::SigMismatch,
                "argument type mismatch",
            );
        }
    };

    // Call closure via suspend/replay
    let ret_buffer = match call.resume_closure_result() {
        Some(cached) => cached,
        None => {
            return ExternResult::CallClosure { closure_ref, args };
        }
    };
    let expected_ret_slots = replay_return_slot_count(call, &ret_value_rttids);
    if ret_buffer.len() != expected_ret_slots {
        let msg = format!(
            "closure replay returned {} slot(s), expected {}",
            ret_buffer.len(),
            expected_ret_slots
        );
        return call_return_error(call, error_offset, DynErr::BadCall, &msg);
    }

    // Pack returns
    if let Err(msg) = pack_returns(
        call,
        &ret_buffer,
        &ret_value_rttids,
        ret_count,
        metas_start,
        is_any_start,
        error_offset,
    ) {
        return call_return_error(call, error_offset, DynErr::BadCall, &msg);
    }
    call_return_success(call, error_offset)
}

#[allow(clippy::too_many_arguments)]
fn do_method(
    call: &mut ExternCallContext,
    base_slot0: u64,
    base_slot1: u64,
    method_name: &str,
    args_slice_ref: GcRef,
    ret_count: usize,
    metas_start: u16,
    is_any_start: u16,
    error_offset: u16,
) -> ExternResult {
    let vk = interface::unpack_value_kind(base_slot0);
    let rttid = interface::unpack_rttid(base_slot0);

    // Try protocol first
    let closure = if let Some(iface_id) =
        check_protocol(call, rttid, vk, call.well_known().attr_object_iface_id)
    {
        match get_method_via_protocol(call, base_slot1, rttid, vk, iface_id, method_name) {
            Ok(c) => Some(c),
            Err(DynOrSuspend::Dyn(e, m)) => {
                return call_return_error(call, error_offset, e, m.borrow());
            }
            Err(DynOrSuspend::Suspend(r)) => return r,
        }
    } else {
        None
    };

    // Fallback to reflection
    let (closure_slot0, closure_slot1) = if let Some(c) = closure {
        c
    } else {
        match get_method(call, rttid, vk, base_slot1, method_name) {
            Ok(c) => c,
            Err((e, m)) => return call_return_error(call, error_offset, e, m),
        }
    };

    // Verify it's a closure
    let closure_vk = interface::unpack_value_kind(closure_slot0);
    if closure_vk != ValueKind::Closure {
        return call_return_error(
            call,
            error_offset,
            DynErr::BadCall,
            &format!("{} is not a method", method_name),
        );
    }

    let closure_rttid = interface::unpack_rttid(closure_slot0);
    let closure_ref = closure_slot1 as GcRef;

    do_call(
        call,
        closure_ref,
        closure_rttid,
        args_slice_ref,
        ret_count,
        metas_start,
        is_any_start,
        error_offset,
    )
}

fn get_method_via_protocol(
    call: &mut ExternCallContext,
    base_slot1: u64,
    rttid: u32,
    vk: ValueKind,
    iface_id: u32,
    method_name: &str,
) -> DynSuspendResult<(u64, u64)> {
    let func_id = protocol_func_id(call, rttid, vk, iface_id, DynErr::BadField)?;
    let name_ref = call.alloc_str(method_name);
    let ret = call_protocol::<4>(
        call,
        func_id,
        &[base_slot1, name_ref as u64],
        DynErr::BadField,
    )?;
    Ok((ret[0], ret[1]))
}

// ============================================================================
// Layer 5: Arg/Return Packing
// ============================================================================

fn unpack_args(
    call: &mut ExternCallContext,
    args_slice_ref: GcRef,
    params: &[vo_common_core::types::ValueRttid],
    is_variadic: bool,
) -> Result<Vec<u64>, ()> {
    let mut args = Vec::new();

    if args_slice_ref.is_null() {
        return Ok(args);
    }

    let arg_count = slice::len(args_slice_ref);
    let elem_bytes = array::elem_bytes(slice::array_ref(args_slice_ref));
    let non_variadic_count = if is_variadic && !params.is_empty() {
        params.len() - 1
    } else {
        params.len()
    };

    // Unpack non-variadic args
    for (i, param) in params
        .iter()
        .enumerate()
        .take(non_variadic_count.min(arg_count))
    {
        let param_vk = param.value_kind();
        let param_slots = call.get_type_slot_count(param.rttid()) as usize;

        let (arg_slot0, arg_slot1) = read_slice_elem(args_slice_ref, i, elem_bytes);
        let raw_arg = prepare_dynamic_value_for_target(
            call,
            *param,
            param_vk,
            param_slots,
            arg_slot0,
            arg_slot1,
            "dynamic call argument",
        )
        .map_err(|_| ())?;
        args.extend(raw_arg);
    }

    // Handle variadic
    if is_variadic && !params.is_empty() {
        let variadic_param = params.last().unwrap();
        let variadic_slice_rttid = variadic_param.rttid();

        let elem_vr = match call.get_slice_elem(variadic_slice_rttid) {
            Some(vr) => vr,
            None => return Ok(args),
        };
        let elem_vk = elem_vr.value_kind();
        let elem_slots = call.get_type_slot_count(elem_vr.rttid()) as usize;
        let elem_meta = call.value_meta_for_value_rttid(elem_vr);

        let variadic_count = arg_count.saturating_sub(non_variadic_count);
        if variadic_count == 0 {
            args.push(core::ptr::null::<u8>() as u64);
        } else {
            let new_slice = slice::create(
                call.gc(),
                elem_meta,
                elem_slots * 8,
                variadic_count,
                variadic_count,
            );
            for i in 0..variadic_count {
                let (arg_slot0, arg_slot1) =
                    read_slice_elem(args_slice_ref, non_variadic_count + i, elem_bytes);
                let raw_arg = prepare_dynamic_value_for_target(
                    call,
                    elem_vr,
                    elem_vk,
                    elem_slots,
                    arg_slot0,
                    arg_slot1,
                    "dynamic variadic argument",
                )
                .map_err(|_| ())?;
                write_slice_elem_raw_slots(new_slice, i, elem_slots * 8, &raw_arg);
            }
            if elem_meta.value_kind().may_contain_gc_refs() {
                call.gc()
                    .mark_allocated_for_scan(slice::array_ref(new_slice));
            }
            args.push(new_slice as u64);
        }
    }

    Ok(args)
}

fn read_slice_elem(slice_ref: GcRef, idx: usize, elem_bytes: usize) -> (u64, u64) {
    let base_ptr = slice::data_ptr(slice_ref);
    let elem_ptr = unsafe { base_ptr.add(idx * elem_bytes) };
    let slot0 = unsafe { *(elem_ptr as *const u64) };
    let slot1 = if elem_bytes >= 16 {
        unsafe { *((elem_ptr as *const u64).add(1)) }
    } else {
        0
    };
    (slot0, slot1)
}

fn replay_value_slot_width(
    call: &ExternCallContext,
    vr: &vo_common_core::types::ValueRttid,
) -> usize {
    match vr.value_kind() {
        ValueKind::Interface => 2,
        ValueKind::Struct | ValueKind::Array => call.get_type_slot_count(vr.rttid()) as usize,
        _ => 1,
    }
}

fn replay_return_slot_count(
    call: &ExternCallContext,
    ret_value_rttids: &[vo_common_core::types::ValueRttid],
) -> usize {
    ret_value_rttids
        .iter()
        .map(|vr| replay_value_slot_width(call, vr))
        .sum()
}

fn pack_returns(
    call: &mut ExternCallContext,
    ret_buffer: &[u64],
    ret_value_rttids: &[vo_common_core::types::ValueRttid],
    ret_count: usize,
    metas_start: u16,
    is_any_start: u16,
    _error_offset: u16,
) -> Result<(), Cow<'static, str>> {
    let mut src_off = 0usize;
    let mut dst_off: u16 = 0;

    for i in 0..ret_count {
        let (_expected_rttid, expected_vk, is_any) =
            get_expected_meta(call, metas_start, is_any_start, i);

        let vr = ret_value_rttids.get(i).ok_or_else(|| {
            Cow::Owned(format!(
                "missing return metadata for dynamic result {} of {}",
                i, ret_count
            ))
        })?;
        let actual_rttid = vr.rttid();
        let actual_vk = vr.value_kind();
        let actual_width = replay_value_slot_width(call, vr);

        let raw_slots = ret_buffer
            .get(src_off..src_off + actual_width)
            .ok_or_else(|| {
                Cow::Owned(format!(
                    "closure replay returned too few slots for dynamic result {}: need slots {}..{}, buffer has {}",
                    i,
                    src_off,
                    src_off + actual_width,
                    ret_buffer.len()
                ))
            })?;
        src_off += actual_width;

        let output_slots = output_slot_count(is_any, expected_vk, actual_width);

        if is_any {
            let boxed = call.box_to_interface(actual_rttid, actual_vk, raw_slots);
            call.ret_any(dst_off, boxed);
        } else if (expected_vk == ValueKind::Struct || expected_vk == ValueKind::Array)
            && actual_width > 2
        {
            let boxed = call.box_to_interface(actual_rttid, actual_vk, raw_slots);
            call.ret_u64(dst_off, 0);
            call.ret_u64(dst_off + 1, boxed.slot1);
        } else {
            if output_slots > 0 {
                call.ret_u64(dst_off, raw_slots[0]);
            }
            if output_slots > 1 {
                call.ret_u64(dst_off + 1, raw_slots[1]);
            }
        }

        dst_off += output_slots;
    }

    if src_off != ret_buffer.len() {
        return Err(Cow::Owned(format!(
            "closure replay returned {} extra slot(s)",
            ret_buffer.len() - src_off
        )));
    }

    Ok(())
}

